const root = document.documentElement;
const translatableNodes = document.querySelectorAll("[data-i18n]");
const languageToggle = document.querySelector(".language-toggle");
const languagePills = document.querySelectorAll("[data-lang-option]");
const savedLanguage = localStorage.getItem("site-language");

function setLanguage(language) {
  const copy = translations[language] || translations.uk;

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (copy[key]) {
      node.textContent = copy[key];
    }
  });

  root.lang = language;
  languagePills.forEach((pill) => {
    pill.classList.toggle("is-active", pill.dataset.langOption === language);
  });

  localStorage.setItem("site-language", language);
}

languageToggle?.addEventListener("click", () => {
  const nextLanguage = root.lang === "uk" ? "en" : "uk";
  setLanguage(nextLanguage);
});

setLanguage(savedLanguage || "uk");
